public class gamepojo {
    int overs;
    int totalScore;
    int player; //

    public gamepojo(int overs, int totalScore, int player) {
        this.totalScore = totalScore;
        this.overs = overs;
        this.player = player;
    }

    public int getOvers() {
        return this.getOvers();
    }

    public int getTotalScore() {
        return this.totalScore;
    }

    public int getplayer() {
        return this.player;
    }
}
